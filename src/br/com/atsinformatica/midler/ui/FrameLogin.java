    private void tSenhaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tSenhaKeyPressed
        if(evt.getKeyCode() != KeyEvent.VK_BACK_SPACE) {            
            if (tSenha.getText().length() > 7){            
                String senha = tSenha.getText();            
                senha = senha.substring(0, 7);
                tSenha.setText(senha);
            }
        }
    }//GEN-LAST:event_tSenhaKeyPressed

    
    /**
     * Valida senha do cliente
     * @return false para senha invalida e true para senha valida
     */
    public boolean validaSenhaCliente(){
       SenhaInstalacaoController senhaController = new SenhaInstalacaoController();
       SenhaSistemaService senhaSistemaService = new SenhaSistemaService();
       try{
          SenhaInstalacaoERPBean senhaInstalacao = senhaController.retornaSenha(); 
          if (senhaInstalacao != null) {
             SenhaSistema senhaSistema = senhaSistemaService.parse(senhaInstalacao.getSenha());
             //verifica se a senha do cliente possui acesso ao midler
             if (!senhaSistema.getSistemas().containsKey("MID")) {
                JOptionPane.showMessageDialog(null, "Esta senha não possui permissão para este sistema!");
                tUsuario.setText("");
                tSenha.setText("");
                tUsuario.requestFocus();
                lMensageLogin.setText("");
                return false;
             }
             Date dataAtual = new Date();
             //se a data de validade da senha esta expirada
             if (dataAtual.after(senhaSistema.getDataValidade())) {
                JOptionPane.showMessageDialog(null, "Senha com data de vencimento expirada!");
                tUsuario.setText("");
                tSenha.setText("");
                tUsuario.requestFocus();
                lMensageLogin.setText("");
                return false;
             }
          }
          Logger.getLogger(FrameLogin.class).info("Senha validada com sucesso!");
          return true;
       }catch(Exception e){
          Logger.getLogger(FrameLogin.class).info("Erro ao validar senha: "+e); 
          return false;
           
       }
       
    }

    public UsuarioERPFacade getUsuarioFacade() {
        if (this.usuarioFacade == null) {
            this.usuarioFacade = new UsuarioERPFacade();
        }
        return usuarioFacade;
    }
    
