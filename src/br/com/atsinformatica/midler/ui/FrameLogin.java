    public static void loginSuperUser(){
        UsuarioERPBean usuarioERPBean = UsuarioERPBean.getInstance();
        usuarioERPBean.setUsuario(UsuarioERPDAO.ADMIN_USUARIO);
        usuarioERPBean.setSenha(UsuarioERPDAO.ADMIN_SENHA);
        usuarioERPBean.setMestre(true);
        
        PanelPrincipal.getInstance();
    }
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        UsuarioERPBean usuarioERPBean = UsuarioERPBean.getInstance();
        
        // Se o usuário é o mestre
        if ((tUsuario.getText().toUpperCase().equals(UsuarioERPDAO.ADMIN_USUARIO))
                && (tSenha.getText().equals(UsuarioERPDAO.ADMIN_SENHA))){
            loginSuperUser();
            this.dispose();
        }
        else {
            usuarioERPBean.setUsuario(tUsuario.getText().trim().toUpperCase());        
            usuarioERPBean.setSenha(Criptogafia.Converter(tSenha.getText()));

            try {
                //valida se usuario existe no sistema
                if (this.getUsuarioFacade().validarUsuario(usuarioERPBean)) {
                    //desabilitado até que a ats possa incluir o midler na senha dos clientes
                    // if(validaSenhaCliente())return;
                    PanelPrincipal.getInstance();
                    this.dispose();                
                } else {
                    lMensageLogin.setText("Login ou Senha Inválida");
                    tUsuario.setText("");
                    tSenha.setText("");
                    tUsuario.requestFocus();
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Erro ao conectar na base de dados.");
                Logger.getLogger(FrameLogin.class).error(ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

