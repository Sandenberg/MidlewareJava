package br.com.atsinformatica.erp.entity;

/**
 *
 * @author AlexsanderPimenta
 */
public class CompclieBean {
    private String codCliente;
    private String enderecoNFE;
    private String complementoNFE;
    private String numeroNFE;

    /**
     * @return the codCliente
     */
    public String getCodCliente() {
        return codCliente;
    }

    /**
     * @param codCliente the codCliente to set
     */
    public void setCodCliente(String codCliente) {
        this.codCliente = codCliente;
    }

    /**
     * @return the enderecoNFE
     */
    public String getEnderecoNFE() {
        return enderecoNFE;
    }

    /**
     * @param enderecoNFE the enderecoNFE to set
     */
    public void setEnderecoNFE(String enderecoNFE) {
        this.enderecoNFE = enderecoNFE;
    }

    /**
     * @return the complementoNFE
     */
    public String getComplementoNFE() {
        return complementoNFE;
    }

    /**
     * @param complementoNFE the complementoNFE to set
     */
    public void setComplementoNFE(String complementoNFE) {
        this.complementoNFE = complementoNFE;
    }

    /**
     * @return the numeroNFE
     */
    public String getNumeroNFE() {
        return numeroNFE;
    }

    /**
     * @param numeroNFE the numeroNFE to set
     */
    public void setNumeroNFE(String numeroNFE) {
        this.numeroNFE = numeroNFE;
    }
    
}
